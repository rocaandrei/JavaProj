    //mai intai vizualizam datele precedente ale contactului vechi si dupa le modificam in buton
    private void updateContactVechi() {

        this.persoanaVechi = contactVechi.getContactPersoana();
        this.telefonPersoanaVechi = contactVechi.getNumarTelefon();

        this.numeJTextField.setText(persoanaVechi.getNume());
        this.prenumeJTextField.setText(persoanaVechi.getPrenume());
        
        //acum luam zilele de nastere vechi ale contactului
        int zi = persoanaVechi.getDataNasterii().getDayOfMonth();
        int luna = persoanaVechi.getDataNasterii().getMonthValue();
        int an = persoanaVechi.getDataNasterii().getYear();

        this.ziJTextField.setText(zi + "");
        this.lunaJTextField.setText(luna + "");
        this.anJTextField.setText(an + "");

        this.numarTelJTextField.setText(this.telefonPersoanaVechi.getNrTel());
    }

    //verificam din nou daca datele noi introduse sunt in regula pentru a fi salvate
    private boolean verificarePersoana(String nume, String prenume) {

        boolean isValid = true;

        //validam numele  de familie
        if (AgendaValidation.validareNume(nume)) {
            //acum validam prenumele
            if (AgendaValidation.validareNume(prenume)) {

                int an = 0;
                int luna = 0;
                int zi = 0;

                try {
                    an = Integer.parseInt(this.anJTextField.getText());
                    luna = Integer.parseInt(this.lunaJTextField.getText());
                    zi = Integer.parseInt(this.ziJTextField.getText());
                } catch (java.lang.NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Data de nastere invalida"
                            + "\nInfo: " + ex.getMessage(), "Data nastere invalida.", JOptionPane.ERROR_MESSAGE);
                    return false;
                }
                //acum validam data de nastere
                if (AgendaValidation.isDateValid(an, luna, zi)) {
                    LocalDate dataNasterii = LocalDate.of(an, luna, zi);
                    try {
                        //daca totul este inregula, setam datele persoanei
                        persoanaVechi = new Persoana(nume, prenume, dataNasterii);
                        isValid = true;

                    } catch (NumePersoanaException ex) {
                        JOptionPane.showMessageDialog(this, "Contactul nu a putut fi setat"
                                + "\nInfo: " + ex.getMessage(), "Eroare.", JOptionPane.ERROR_MESSAGE);
                        isValid = false;
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Data de nastere nu este valida"
                            + "\nMai incercati...", "Data nastere invalida.", JOptionPane.INFORMATION_MESSAGE);
                    isValid = false;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Prenumele nu este valid"
                        + "\nMai incercati...", "Nume faimilie invalid", JOptionPane.INFORMATION_MESSAGE);
                isValid = false;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Numele de familie nu este valid"
                    + "\nMai incercati...", "Nume faimilie invalid", JOptionPane.INFORMATION_MESSAGE);
            isValid = false;
        }
        return isValid;
    }

