    private void ModificaContactJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificaContactJButtonActionPerformed

        int clickOption = JOptionPane.showConfirmDialog(this, "Sunteti sigur ca doriti sa salvati modificarea contactului?",
                "Modificare contact", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        switch (clickOption) {
            case 0:
                if (agendaPrimara != null) {
                    String nume = this.numeJTextField.getText();
                    String prenume = this.prenumeJTextField.getText();
                    boolean isValid = verificarePersoana(nume, prenume);

                    String numarTel = this.numarTelJTextField.getText();
                    if (isValid) {
                        if (nrFixJRadioButton.isSelected()) {
                            telefonPersoanaVechi = new NrFix(numarTel);
                        } else if (nrMobilJRadioButton.isSelected()) {
                            telefonPersoanaVechi = new NrMobil(numarTel);
                        } else {
                            JOptionPane.showMessageDialog(this, "Va rugam sa bifati tipul numarului de telefon",
                                    "Setare numar telefon.", JOptionPane.INFORMATION_MESSAGE);
                            return;
                        }
                        if (telefonPersoanaVechi.getNrTel().equals("N/A")) {
                            JOptionPane.showMessageDialog(this, "Numarul de telefon nu se afla in formatul corect sau nu respecta tipul bifat (mobil/fix).\nAti introdus: " + numarTel + "\nMai incercati...",
                                    "Setare numar telefon.", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            contactNou = new Contact(persoanaVechi, telefonPersoanaVechi);
                            JOptionPane.showMessageDialog(this, "Contactul a fost modificat!",
                                    "Contact modificat!", JOptionPane.INFORMATION_MESSAGE);

                            //si dupa revenim la agenda initiala prin apelarea ei. - 
                            //TODO: nu prea imi place ca pot sa accesez in paralel si form-ul din urma,.. sa verific cum il blochez cat timp sunt in asta.
                            agendaPrimara.agenda.modificaContact(contactVechi, contactNou);
                            agendaPrimara.updateAgendaModel();
                            this.setVisible(false);
                        }
                    }
                }
                break;
            case 1:
                this.setVisible(false);
                break;
        }
    }//GEN-LAST:event_ModificaContactJButtonActionPerformed

