    private boolean verificarePersoana() {

        String nume = this.numeJTextField.getText();
        String prenume = this.prenumeJTextField.getText();
        boolean isValid;

        //validam numele  de familie
        if (AgendaValidation.validareNume(nume)) {
            //acum validam prenumele
            if (AgendaValidation.validareNume(prenume)) {

                int an = 0;
                int luna = 0;
                int zi = 0;

                try {
                    an = Integer.parseInt(this.anJTextField.getText());
                    luna = Integer.parseInt(this.lunaJTextField.getText());
                    zi = Integer.parseInt(this.ziJTextField.getText());
                } catch (java.lang.NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Data de nastere invalida"
                            + "\nInfo: " + ex.getMessage(), "Data nastere invalida.", JOptionPane.ERROR_MESSAGE);
                    return false;
                }
                //acum validam data de nastere
                if (AgendaValidation.isDateValid(an, luna, zi)) {
                    LocalDate dataNasterii = LocalDate.of(an, luna, zi);
                    try {
                        //daca totul este inregula, setam datele persoanei
                        persoana = new Persoana(nume, prenume, dataNasterii);
                        isValid = true;

                    } catch (NumePersoanaException ex) {
                        JOptionPane.showMessageDialog(this, "Contactul nu a putut fi setat"
                                + "\nInfo: " + ex.getMessage(), "Eroare.", JOptionPane.ERROR_MESSAGE);
                        isValid = false;
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Data de nastere nu este valida"
                            + "\nMai incercati...", "Data nastere invalida.", JOptionPane.INFORMATION_MESSAGE);
                    isValid = false;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Prenumele nu este valid"
                        + "\nMai incercati...", "Prenume invalid", JOptionPane.INFORMATION_MESSAGE);
                isValid = false;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Numele de familie nu este valid"
                    + "\nMai incercati...", "Nume familie invalid", JOptionPane.INFORMATION_MESSAGE);
            isValid = false;
        }

        return isValid;
    }

    private boolean verificareDuplicat() {

        String nume = this.numeJTextField.getText();
        String prenume = this.prenumeJTextField.getText();
        String telefon = this.numarTelJTextField.getText();

        boolean isDuplicate = false;

        for (Contact contact : this.agendaPrimara.contacte) {
            if (nume.equals(contact.getContactPersoana().getNume())
                    && prenume.equals(contact.getContactPersoana().getPrenume())) {
                isDuplicate = true;
                break;
            } else {
                if (telefon.equals(contact.getNumarTelefon().getNrTel())) {
                    isDuplicate = true;
                    break;
                } else {
                    isDuplicate = false;
                }
            }
        }
        return isDuplicate;
    }

    //verificam daca la adaugare este ziua de nastere a contactului si afisam un mesaj daca este ziua lui
    private void urareLMA() {
        if (Integer.parseInt(this.ziJTextField.getText()) == LocalDate.now().getDayOfMonth()
                && Integer.parseInt(this.lunaJTextField.getText()) == LocalDate.now().getMonthValue()) {
            JOptionPane.showMessageDialog(this, "Astazi este ziua lui " + contactNou.getContactPersoana().getFullName(CriteriuOrdonare.DUPA_NUME)
                    + "\nUreaza-i la multi ani :)",
                    "La multi ani!", JOptionPane.INFORMATION_MESSAGE);
        }
    }


