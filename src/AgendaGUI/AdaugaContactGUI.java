    private void adaugaContactJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adaugaContactJButtonActionPerformed

        boolean isPersonValid = verificarePersoana();
        String numarTel = this.numarTelJTextField.getText();

        if (isPersonValid) {
            if (nrFixJRadioButton.isSelected()) {
                telefonPersoana = new NrFix(numarTel);
            } else if (nrMobilJRadioButton.isSelected()) {
                telefonPersoana = new NrMobil(numarTel);
            } else {
                JOptionPane.showMessageDialog(this, "Va rugam sa bifati tipul numarului de telefon",
                        "Setare numar telefon.", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if (telefonPersoana.getNrTel().equals("N/A")) {
                JOptionPane.showMessageDialog(this, "Numarul de telefon nu se afla in formatul corect sau nu respecta tipul bifat (mobil/fix).\nAti introdus: " + numarTel + "\nMai incercati...",
                        "Setare numar telefon.", JOptionPane.INFORMATION_MESSAGE);
            } else {
                contactNou = new Contact(persoana, telefonPersoana);

                if (!verificareDuplicat()) {
                    agendaPrimara.agenda.adaugaContact(contactNou);

                    //si dupa revenim la agenda initiala prin apelarea ei. - TODO: nu prea imi place ca pot sa accesez in paralel si form-ul din urma,.. sa verific cum il blochez cat timp sunt in asta.
                    agendaPrimara.updateAgendaModel();
                    urareLMA();
                    JOptionPane.showMessageDialog(this, "Contactul creat a fost adaugat in lista",
                            "Contact adaugat!", JOptionPane.INFORMATION_MESSAGE);

                    this.setVisible(false);

                } else {
                    JOptionPane.showMessageDialog(this, "Contactul exista deja in agenda!\nMai incercati.",
                            "Contact existent!", JOptionPane.ERROR_MESSAGE);
                    this.setVisible(false);
                }
            }

        }
    }//GEN-LAST:event_adaugaContactJButtonActionPerformed

